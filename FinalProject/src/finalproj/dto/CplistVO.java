package finalproj.dto;

public class CplistVO {
	private int keynum;
	private String cpnum;
	private String cpname;
	private String cploc;
	/**
	 * @return the keynum
	 */
	public int getKeynum() {
		return keynum;
	}
	/**
	 * @param keynum the keynum to set
	 */
	public void setKeynum(int keynum) {
		this.keynum = keynum;
	}
	/**
	 * @return the cpnum
	 */
	public String getCpnum() {
		return cpnum;
	}
	/**
	 * @param cpnum the cpnum to set
	 */
	public void setCpnum(String cpnum) {
		this.cpnum = cpnum;
	}
	/**
	 * @return the cpname
	 */
	public String getCpname() {
		return cpname;
	}
	/**
	 * @param cpname the cpname to set
	 */
	public void setCpname(String cpname) {
		this.cpname = cpname;
	}
	/**
	 * @return the cploc
	 */
	public String getCploc() {
		return cploc;
	}
	/**
	 * @param cploc the cploc to set
	 */
	public void setCploc(String cploc) {
		this.cploc = cploc;
	}
	
	
}
